import pandas as pd
import statsmodels.api as sm

# 1. 데이터 불러오기
df = pd.read_csv("data/converted_data.csv")

# 2. 요인별 점수 계산
df["psm_score"] = df[["q30_1", "q30_2", "q30_3", "q30_4", "q30_5", "q30_6"]].mean(axis=1)
df["edg_score"] = df[["q31_1", "q31_2", "q31_3", "q31_4", "q31_5", "q31_6", "q31_7"]].mean(axis=1)
df["om_score"] = df[["q35_1", "q35_2", "q35_3", "q35_4"]].mean(axis=1)

# 3. 독립변수 + 통제변수 포함
X = df[[
    "psm_score", "edg_score",     # 독립변수
    "dq1", "dq2", "dq3",          # 통제변수 1 (출생년도, 성별, 혼인상태)
    "dq5_1", "dq5_2",             # 통제변수 2 (학력 입직/현재)
    "dq9_2", "dq10"               # 통제변수 3 (직급, 급여수준)
]]

# 4. 더미변수화 (범주형 통제변수)
X = pd.get_dummies(X, columns=["dq2", "dq3", "dq5_1", "dq5_2", "dq9_2", "dq10"], drop_first=True)

# 5. 상수항 추가
X = sm.add_constant(X)

# 6. 종속변수 지정
y = df["om_score"]

# 7. 숫자형으로 변환
X = X.astype(float)
y = y.astype(float)

# 8. 회귀모델 적합
model = sm.OLS(y, X).fit()

# 9. 결과 출력
print(model.summary())


